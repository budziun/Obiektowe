public class Kalkulator {
    public static int x1;
    public static int x2;

    public static int dodawanie(int x1,int x2){
        return x1+x2;
    }
    public static int odejmowanie(int x1,int x2){
        return x1-x2;
    }
    public static int mnozenie(int x1,int x2){
        return x1*x2;
    }
    public static int dzielenie(int x1,int x2){
        return x1/x2;
    }
}
